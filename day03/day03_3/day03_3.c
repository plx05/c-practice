void func() {
    int a = 10;       // ? a 的作用域开始

    {                 // --- 大括号1 (内层) 开始 ---
        int b = 20;   // ? b 的作用域开始

        printf("%d", a); // ? 合法：内层可以访问外层的 a
        printf("%d", b); // ? 合法：b 在当前作用域内

        {             // --- 大括号2 (更内层) 开始 ---
            int c = 30;
            printf("%d", a); // ? 合法
            printf("%d", b); // ? 合法
            printf("%d", c); // ? 合法
        }             // --- 大括号2 结束，c 被销毁 ? ---

        // printf("%d", c); // ? 编译错误！c 已经不存在了
    }                 // --- 大括号1 结束，b 被销毁 ? ---

    // printf("%d", b); // ? 编译错误！b 已经不存在了
    printf("%d", a);   // ? 合法：a 仍然有效
}                     // a 被销毁